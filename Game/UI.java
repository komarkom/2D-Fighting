    private void blockAttackCheck()
    {
        if (countAttack != 2)
        {
            for(int i = 0; i < 6; ++i)
            {
                attackList[i].setEnabled(true);
            }
        }
        else
        {
            for(int i = 0; i < 6; ++i)
            {
                if(!attackList[i].isSelected())
                    attackList[i].setEnabled(false);
            }
        }
    }
    private void blockDefenseCheck()
    {
        if (countDefense != 2)
        {
            for(int i = 0; i < 6; ++i)
            {
                defenseList[i].setEnabled(true);
            }
        }
        else
        {
            for(int i = 0; i < 6; ++i)
            {
                if(!defenseList[i].isSelected())
                    defenseList[i].setEnabled(false);
            }
        }
    }
    
   
