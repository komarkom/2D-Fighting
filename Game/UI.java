    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            boolean[] attack = new boolean[6];
            boolean[] defense = new boolean[6];
            
            for (int i = 0; i < 6; ++i)
            {
                attack[i] = attackList[i].isSelected();
                defense[i] = defenseList[i].isSelected();
            }
        
            
            battleLog += server.translateMessage(new Message(attack, defense, bot.choiceAttack(), bot.choiceDefense())) + "\n";
            
            
            jLabel6.setText("HP: " + Integer.toString(player.currentHp) + "/" + Integer.toString(player.maxHp));
            jLabel7.setText("HP: " + Integer.toString(bot.currentHp) + "/" + Integer.toString(bot.maxHp));
            
            if (player.currentHp <= 0)
            {
                battleLog += "Бой окончен! Игрок проиграл.\nТак же вы потратили " +  Long.toString(System.currentTimeMillis()) 
                          + " миллисекунд своей жизни.\n";
                player.currentHp = player.maxHp;
                bot.currentHp = bot.maxHp;
            }
            else if (bot.currentHp <= 0)
            {
                battleLog += "Бой окончен! Бот проиграл.\n"+  Long.toString(System.currentTimeMillis()) 
                          + " миллисекунд своей жизни.\n"; 
                player.currentHp = player.maxHp;
                bot.currentHp = bot.maxHp;
            }
            if (player.currentHp <= 0 && bot.currentHp <= 0)
            {
                battleLog += "Бой окончен! Ничья.\n" +  Long.toString(System.currentTimeMillis()) 
                          + " миллисекунд своей жизни.\n";
                player.currentHp = player.maxHp;
                bot.currentHp = bot.maxHp;
            }
            logger.setText(battleLog);
    }//GEN-LAST:event_jButton1ActionPerformed

